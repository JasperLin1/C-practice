#include <iostream>
#include <cstdlib>   // 包含 rand() 和 srand() 用於隨機數
#include <ctime>     // 包含 time() 用於時間種子

// ========================================================
// 🎯 Week 1 整合小專案：猜數字遊戲與歷史紀錄
// ========================================================
// 這是你第一週的總結專案！它將會結合以下觀念：
// 1. 變數與資料型態 (儲存目標數字、玩家的猜測、歷史紀錄)
// 2. 條件判斷 (if-else 提示玩家太高或太低)
// 3. 迴圈控制 (while 迴圈讓玩家重複猜，直到猜對為止；for 迴圈印出歷史紀錄)
// 4. 陣列 (儲存玩家的猜測歷史)
//
// 遊戲規則：
// - 電腦隨機生成一個 1 到 100 的數字。
// - 玩家輸入數字進行猜測。
// - 若猜錯，提示「太高了！」或「太低了！」，並將此次猜測記錄下來。
// - 若猜對，恭喜玩家，並印出玩家總共猜了幾次，以及「每一次猜測的歷史紀錄」。
// ========================================================
// 🎯 Week 1 整合小專案：猜數字遊戲與歷史紀錄
// ========================================================

// 宣告獨立的遊戲函式 (採用我們討論的方案 A：所有變數都在函式內部宣告)
void guessGame() {
    // 1. 產生 1 到 100 的隨機數字
    int target = std::rand() % 100 + 1;

    // 2. 宣告遊戲所需的變數
    int guess;                         // 儲存玩家當前的猜測
    int guessCount = 0;                // 紀錄玩家猜了幾次
    int history[100];                  // 儲存每次猜測數字的陣列

    // 3. 遊戲主迴圈
    while (true) {
        std::cout << "請輸入你的猜測 (1-100): ";
        std::cin >> guess;

        // 紀錄這次猜測到陣列中
        history[guessCount] = guess;
        guessCount += 1;

        // 條件判斷大小
        if (guess < target) {
            std::cout << "太小了！再試一次。" << std::endl;
        } else if (guess > target) {
            std::cout << "太高了！再試一次。" << std::endl;
        } else {
            // 猜對了 (guess == target)
            std::cout << "\n🎉 恭喜你！猜對了！" << std::endl;
            break; // 跳出 while 迴圈
        }
    }

    // 4. 印出結算報告與歷史紀錄
    std::cout << "\n========================================" << std::endl;
    std::cout << "📊 遊戲結算報告" << std::endl;
    std::cout << "========================================" << std::endl;
    std::cout << "你總共猜了 " << guessCount << " 次！" << std::endl;
    
    std::cout << "你的所有猜測歷史紀錄：" << std::endl;
    // 關鍵：必須使用 for 迴圈逐一印出陣列裡的數，直接印 history 只會印出記憶體位址
    for (int i = 0; i < guessCount; i++) {
        std::cout << "第 " << (i + 1) << " 次猜測: " << history[i] << std::endl;
    }
}

int main() {
    // 初始化隨機數種子（使用當前時間，確保每次執行數字都不同）
    std::srand(static_cast<unsigned int>(std::time(nullptr)));
    
    std::cout << "========================================" << std::endl;
    std::cout << "🎮 歡迎來到猜數字遊戲 (1-100)！" << std::endl;
    std::cout << "========================================" << std::endl;

    // 呼叫我們封裝好的遊戲函式
    guessGame();

    return 0;
}
